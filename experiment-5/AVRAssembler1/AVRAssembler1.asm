 .INCLUDE "M32DEF.INC"


 LDI R16, HIGH(RAMEND)
 OUT SPH, R16
 LDI R16, LOW(RAMEND)
 OUT SPL, R16
 LDI R16, 0xFF
 OUT DDRB, R16
 LDI R20, 0x00
 LI: 
	 OUT PORTB, R20
	 CALL DELAY
	 INC R20
	 BRNE LI
DELAY:
	LDI R18, 2
AGAIN:
	NOP
	NOP
	DEC R18
BRNE AGAIN
RET